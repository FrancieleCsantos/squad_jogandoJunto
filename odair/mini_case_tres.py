valor_digitado=float(input("Digite um valor: "))
dobro=valor_digitado * 2
triplo=valor_digitado * 3
quadrado=valor_digitado ** 2
raiz_quadrada=valor_digitado ** (1/2)
raiz_cubica=valor_digitado ** (1/3)
print(f"O dobro de {valor_digitado} é: {dobro:.0f}")
print(f"O triplo de {valor_digitado} é: {triplo:.0f}")
print(f"O quadrado de {valor_digitado} é: {quadrado:.0f}")
print(f"A raiz quadrada de {valor_digitado} é: {raiz_quadrada}")
print(f"A raiz cúbica de {valor_digitado} é: {raiz_cubica}")