valor_inicial = float(input("Olá! Digite um valor: "))
valor_dobro = valor_inicial * 2
print(f"O valor {valor_dobro} é o dobro do valor {valor_inicial}!")
valor_triplo = valor_inicial * 3
print(f"O valor {valor_triplo} é o triplo do valor {valor_inicial}!")
valor_quadrado = valor_inicial ** 2
print(f"O valor {valor_quadrado} é o valor ao quadrado do valor {valor_inicial}!")
valor_raiz_quadrada = valor_inicial ** 0.5
print(f"O valor {valor_raiz_quadrada} é a raiz quadrada do valor {valor_inicial}!")
valor_raiz_cubica = valor_inicial ** (1/3)
print(f"O valor {valor_raiz_cubica:.2f} é a raiz cúbica do valor {valor_inicial}!")